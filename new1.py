import pandas as pd

# Створюємо просту таблицю (DataFrame)
data = {
    'Ім\'я': ['Олег', 'Анна', 'Іван'],
    'Вік': [23, 25, 21],
    'Місто': ['Львів', 'Київ', 'Одеса']
}
a = 30
# Конвертуємо словник у DataFrame
df = pd.DataFrame(data)

# Виводимо таблицю
print(df)


# Фільтруємо: показуємо лише тих, кому більше 22 років
older_than_22 = df[df['Вік'] > 22]
print("\nЛюди старші за 22 роки:")
print(f"вивід змінної: {a}")
print(older_than_22)