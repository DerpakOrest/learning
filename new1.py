# ‚úÖ –Ü–º–ø–æ—Ä—Ç –±—ñ–±–ª—ñ–æ—Ç–µ–∫
import pandas as pd
import numpy as np
import matplotlib.pyplot as plt

# ----------------------------------------------
# üìä 1. –°–¢–í–û–†–ï–ù–ù–Ø DATAFRAME (–í–ª–∞—Å–Ω–∏–π –Ω–∞–±—ñ—Ä –¥–∞–Ω–∏—Ö)
# ----------------------------------------------
data = pd.DataFrame({
    'Product': ['Laptop', 'Smartphone', 'Tablet', 'Monitor', 'Keyboard', 'Mouse', 'Printer', 'Headphones'],
    'Price': [1200, 800, 400, 300, 50, 40, 200, 150],
    'Units Sold': [150, 250, 180, 120, 300, 350, 90, 200]
})

# –í–∏–≤—ñ–¥ DataFrame
print("üìã DataFrame:")
print(data)

# ----------------------------------------------
# üìè 2. –û–ë–ß–ò–°–õ–ï–ù–ù–Ø –°–ï–†–ï–î–ù–Ü–•, –ú–Ü–ù–Ü–ú–ê–õ–¨–ù–ò–• –¢–ê –ú–ê–ö–°–ò–ú–ê–õ–¨–ù–ò–• –ó–ù–ê–ß–ï–ù–¨
# ----------------------------------------------
# –°–µ—Ä–µ–¥–Ω—î –∑–Ω–∞—á–µ–Ω–Ω—è
mean_price = data['Price'].mean()
mean_units = data['Units Sold'].mean()

# –ú—ñ–Ω—ñ–º–∞–ª—å–Ω–µ –∑–Ω–∞—á–µ–Ω–Ω—è
min_price = data['Price'].min()
min_units = data['Units Sold'].min()

# –ú–∞–∫—Å–∏–º–∞–ª—å–Ω–µ –∑–Ω–∞—á–µ–Ω–Ω—è
max_price = data['Price'].max()
max_units = data['Units Sold'].max()

# –í–∏–≤—ñ–¥ —Ä–µ–∑—É–ª—å—Ç–∞—Ç—ñ–≤
print("\nüìä –°–µ—Ä–µ–¥–Ω—î –∑–Ω–∞—á–µ–Ω–Ω—è:")
print(f"üí∞ –°–µ—Ä–µ–¥–Ω—è —Ü—ñ–Ω–∞: {mean_price}")
print(f"üì¶ –°–µ—Ä–µ–¥–Ω—è –∫—ñ–ª—å–∫—ñ—Å—Ç—å –ø—Ä–æ–¥–∞–Ω–∏—Ö –æ–¥–∏–Ω–∏—Ü—å: {mean_units}")

print("\nüîª –ú—ñ–Ω—ñ–º–∞–ª—å–Ω—ñ –∑–Ω–∞—á–µ–Ω–Ω—è:")
print(f"üí∞ –ú—ñ–Ω—ñ–º–∞–ª—å–Ω–∞ —Ü—ñ–Ω–∞: {min_price}")
print(f"üì¶ –ú—ñ–Ω—ñ–º–∞–ª—å–Ω–∞ –∫—ñ–ª—å–∫—ñ—Å—Ç—å –ø—Ä–æ–¥–∞–Ω–∏—Ö –æ–¥–∏–Ω–∏—Ü—å: {min_units}")

print("\nüî∫ –ú–∞–∫—Å–∏–º–∞–ª—å–Ω—ñ –∑–Ω–∞—á–µ–Ω–Ω—è:")
print(f"üí∞ –ú–∞–∫—Å–∏–º–∞–ª—å–Ω–∞ —Ü—ñ–Ω–∞: {max_price}")
print(f"üì¶ –ú–∞–∫—Å–∏–º–∞–ª—å–Ω–∞ –∫—ñ–ª—å–∫—ñ—Å—Ç—å –ø—Ä–æ–¥–∞–Ω–∏—Ö –æ–¥–∏–Ω–∏—Ü—å: {max_units}")

# ----------------------------------------------
# üìà 3. –ì–†–ê–§–Ü–ö: –ü–û–†–Ü–í–ù–Ø–ù–ù–Ø –î–í–û–• –ü–ê–†–ê–ú–ï–¢–†–Ü–í (–ù–æ–≤–∏–π —Å—Ç–∏–ª—å)
# ----------------------------------------------
plt.figure(figsize=(14, 7))
colors = plt.cm.Paired(np.linspace(0, 1, len(data['Product'])))

# üü¶ –°—Ç–æ–≤–ø—á–∞—Å—Ç–∞ –¥—ñ–∞–≥—Ä–∞–º–∞ –¥–ª—è –∫—ñ–ª—å–∫–æ—Å—Ç—ñ –ø—Ä–æ–¥–∞–∂—ñ–≤
bars = plt.bar(data['Product'], data['Units Sold'], color=colors, label='Units Sold', alpha=0.8)

# üî¥ –õ—ñ–Ω—ñ–π–Ω–∏–π –≥—Ä–∞—Ñ—ñ–∫ –¥–ª—è —Ü—ñ–Ω
plt.plot(data['Product'], data['Price'], color='red', marker='D', linestyle='--', linewidth=2, label='Price ($)')

# üí° –î–æ–¥–∞–≤ –ø—ñ–¥–ø–∏—Å–∏ –Ω–∞ —Å—Ç–æ–≤–ø—Ü—è—Ö
for bar in bars:
    height = bar.get_height()
    plt.text(bar.get_x() + bar.get_width() / 2, height + 5, f'{height}', ha='center', fontsize=9)

# –ó–∞–≥–æ–ª–æ–≤–∫–∏ —Ç–∞ –ø—ñ–¥–ø–∏—Å–∏
plt.title('üí∞ –ü–æ—Ä—ñ–≤–Ω—è–Ω–Ω—è –¶—ñ–Ω–∏ —Ç–∞ –ü—Ä–æ–¥–∞–∂—ñ–≤ –∑–∞ –ü—Ä–æ–¥—É–∫—Ç–∞–º–∏', fontsize=16)
plt.xlabel('–ü—Ä–æ–¥—É–∫—Ç', fontsize=12)
plt.ylabel('–ó–Ω–∞—á–µ–Ω–Ω—è', fontsize=12)
plt.legend()
plt.grid(axis='y', linestyle='--', alpha=0.7)
plt.show()

# ----------------------------------------------
# ‚ûó 4. –í–ò–ö–û–†–ò–°–¢–ê–ù–ù–Ø NUMPY –î–õ–Ø –ú–ê–¢–ï–ú–ê–¢–ò–ß–ù–ò–• –û–ü–ï–†–ê–¶–Ü–ô
# ----------------------------------------------
# –ü–µ—Ä–µ—Ç–≤–æ—Ä–µ–Ω–Ω—è —Å—Ç–æ–≤–ø—Ü—è "–¶—ñ–Ω–∞" –≤ –º–∞—Å–∏–≤ NumPy
price_array = np.array(data['Price'])

# –û–ø–µ—Ä–∞—Ü—ñ—ó –∑ NumPy
total_revenue = np.sum(price_array * data['Units Sold'])  # –ó–∞–≥–∞–ª—å–Ω–∏–π –¥–æ—Ö—ñ–¥
std_dev_price = np.std(price_array)  # –°—Ç–∞–Ω–¥–∞—Ä—Ç–Ω–µ –≤—ñ–¥—Ö–∏–ª–µ–Ω–Ω—è —Ü—ñ–Ω–∏
normalized_price = (price_array - np.mean(price_array)) / np.std(price_array)  # –ù–æ—Ä–º–∞–ª—ñ–∑–∞—Ü—ñ—è

# –í–∏–≤—ñ–¥ —Ä–µ–∑—É–ª—å—Ç–∞—Ç—ñ–≤
print("\nüíµ –í–∏–∫–æ—Ä–∏—Å—Ç–∞–Ω–Ω—è NumPy:")
print(f"üí≤ –ó–∞–≥–∞–ª—å–Ω–∏–π –¥–æ—Ö—ñ–¥: {total_revenue}")
print(f"üìè –°—Ç–∞–Ω–¥–∞—Ä—Ç–Ω–µ –≤—ñ–¥—Ö–∏–ª–µ–Ω–Ω—è —Ü—ñ–Ω–∏: {std_dev_price:.2f}")
print(f"üìä –ù–æ—Ä–º–∞–ª—ñ–∑–æ–≤–∞–Ω—ñ –∑–Ω–∞—á–µ–Ω–Ω—è —Ü—ñ–Ω–∏: {normalized_price}")
